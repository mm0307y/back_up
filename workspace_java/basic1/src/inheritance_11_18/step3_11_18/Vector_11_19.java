package inheritance_11_18.step3_11_18;

public class Vector_11_19 {
    private  int x;
    private  int y;

    //생성자 호출을 통해서 멤버변수의 초기화가 가능하다.
   public  Vector_11_19(int x, int y) {
       this.x = x;
       this.y = y;
   }
   public int getX() {
       return this. x;
   }
   public int getY() {
       return this.y;
   }
}
